#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		nbr;
	t_list	*p;

	nbr = 0;
	if (lst)
	{
		nbr ++;
		p = lst->next;
		while (p)
		{
			nbr ++;
			p = p->next;
		}
	}
	return (nbr);
}
